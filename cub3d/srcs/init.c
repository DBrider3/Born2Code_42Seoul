#include "cub3d.h"

void	init_identifier(t_options *op)
{
	int		i;

	i = 0;
	while (i < 2)
		op->r[i++] = 0;
	op->no = 0;
	op->so = 0;
	op->we = 0;
	op->ea = 0;
	op->s = 0;
	i = 0;
	while (i < 3)
	{
		op->f[i] = -1;
		op->c[i] = -1;
		i++;
	}
	op->map = 0;
}
